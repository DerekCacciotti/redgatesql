DischargeReasonSpecify = @DischargeReasonSpecify, 
DischargeSafetyReason = @DischargeSafetyReason, 
DischargeSafetyReasonDV = @DischargeSafetyReasonDV, 
DischargeSafetyReasonMH = @DischargeSafetyReasonMH, 
