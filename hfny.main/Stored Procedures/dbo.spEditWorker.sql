CREATE PROCEDURE [dbo].[spEditWorker](@WorkerPK int=NULL,
@Apt char(10)=NULL,
@ASQTrainingDate datetime=NULL,
@CellPhone char(12)=NULL,
@Children bit=NULL,
@City char(20)=NULL,
@EducationLevel char(2)=NULL,
@FAWCoreDate datetime=NULL,
@FAWInitialStart datetime=NULL,
@FirstName char(20)=NULL,
@FSWCoreDate datetime=NULL,
@FSWInitialStart datetime=NULL,
@FTE char(2)=NULL,
@FTEFullTime bit=NULL,
@Gender char(2)=NULL,
@HomePhone char(12)=NULL,
@LanguageSpecify varchar(100)=NULL,
@LastName char(30)=NULL,
@OtherLanguage bit=NULL,
@PreviousName char(51)=NULL,
@Race char(2)=NULL,
@RaceSpecify varchar(500)=NULL,
@State char(2)=NULL,
@Street char(30)=NULL,
@SupervisorCoreDate datetime=NULL,
@SupervisorFirstEvent datetime=NULL,
@SupervisorInitialStart datetime=NULL,
@WorkerDOB datetime=NULL,
@WorkerEditor char(10)=NULL,
@WorkerPK_old int=NULL,
@YoungestChild int=NULL,
@Zip char(10)=NULL,
@LoginCreated bit=NULL,
@YearsHVExperience int=NULL,
@YearsEarlyChildhoodExperience int=NULL,
@YearsChildAbuseClasses int=NULL,
@SupervisionScheduledDay int=NULL)
AS
UPDATE Worker
SET 
Apt = @Apt, 
ASQTrainingDate = @ASQTrainingDate, 
CellPhone = @CellPhone, 
Children = @Children, 
City = @City, 
EducationLevel = @EducationLevel, 
FAWCoreDate = @FAWCoreDate, 
FAWInitialStart = @FAWInitialStart, 
FirstName = @FirstName, 
FSWCoreDate = @FSWCoreDate, 
FSWInitialStart = @FSWInitialStart, 
FTE = @FTE, 
FTEFullTime = @FTEFullTime, 
Gender = @Gender, 
HomePhone = @HomePhone, 
LanguageSpecify = @LanguageSpecify, 
LastName = @LastName, 
OtherLanguage = @OtherLanguage, 
PreviousName = @PreviousName, 
Race = @Race, 
RaceSpecify = @RaceSpecify, 
State = @State, 
Street = @Street, 
SupervisorCoreDate = @SupervisorCoreDate, 
SupervisorFirstEvent = @SupervisorFirstEvent, 
SupervisorInitialStart = @SupervisorInitialStart, 
WorkerDOB = @WorkerDOB, 
WorkerEditor = @WorkerEditor, 
WorkerPK_old = @WorkerPK_old, 
YoungestChild = @YoungestChild, 
Zip = @Zip, 
LoginCreated = @LoginCreated, 
YearsHVExperience = @YearsHVExperience, 
YearsEarlyChildhoodExperience = @YearsEarlyChildhoodExperience, 
YearsChildAbuseClasses = @YearsChildAbuseClasses, 
SupervisionScheduledDay = @SupervisionScheduledDay
WHERE WorkerPK = @WorkerPK
GO
